function split(text, divider) {
    // Створюємо пустий масив для збереження розділених частин рядка.
    let result = [];

    // Створюємо змінну, яка буде використовуватися для зберігання поточного фрагмента рядка.
    let currentChunk = '';

    // Проходимося по всім символам у рядку.
    for (let  i = 0; i < text.length; i++) {
        // Перевіряємо, чи поточний символ дорівнює роздільнику.
        if (text[i] === divider) {
            // Якщо так, додаємо поточний фрагмент до масиву та очищуємо його.
            result.push(currentChunk);
            currentChunk = '';
        } else {
            // Якщо символ не є роздільником, додаємо його до поточного фрагмента.
            currentChunk += text[i];
        }
    }

    // Після завершення обходу рядка, додаємо останній фрагмент, який залишився, до масиву, якщо він не пустий.
    if (currentChunk !== '') {
        result.push(currentChunk);
    }

    // Повертаємо масив з розділеними частинами рядка.
    return result;
}

// Приклад використання:
let text = "Розбиття тексту на слова";
let divider = " ";
let parts = split(text, divider);
console.log(parts); // Результат: ["Розбиття", "тексту", "на", "слова"]